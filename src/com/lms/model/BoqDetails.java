package com.lms.model;

public class BoqDetails {
	
	private String boq_hd_id ,
	  boq_detail_id ,
	  title ,
	  unit,
	  quantity,
	  amount,
	  remarks;

	public String getBoq_hd_id() {
		return boq_hd_id;
	}

	public void setBoq_hd_id(String boq_hd_id) {
		this.boq_hd_id = boq_hd_id;
	}

	public String getBoq_detail_id() {
		return boq_detail_id;
	}

	public void setBoq_detail_id(String boq_detail_id) {
		this.boq_detail_id = boq_detail_id;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getUnit() {
		return unit;
	}

	public void setUnit(String unit) {
		this.unit = unit;
	}

	public String getQuantity() {
		return quantity;
	}

	public void setQuantity(String quantity) {
		this.quantity = quantity;
	}

	public String getAmount() {
		return amount;
	}

	public void setAmount(String amount) {
		this.amount = amount;
	}

	public String getRemarks() {
		return remarks;
	}

	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}
	
	

}
